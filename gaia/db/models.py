"""
gaia/db/models.py
=================
GAIA-OS ORM model stubs.
Canon reference: C04 (Human/Gaian Twin Architecture),
                 C17 (Persistent Memory and Identity Architecture)

These are typed dataclass stubs that mirror the SQLAlchemy / Alembic
schema defined in the full DB layer. They are used by:
  - tests that import from gaia.db or gaia.db.models
  - API layer serialization helpers
  - Any module that needs typed memory / audit / session objects
    without pulling in the full SQLAlchemy dependency chain.

The real SQLAlchemy models live in gaia/db/orm/ (generated by Alembic).
When those models are fully wired, these stubs will be replaced by
direct re-exports from gaia.db.orm.
"""

from __future__ import annotations

import time
import uuid
from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional


# ---------------------------------------------------------------------------
# MemoryRecord
# ---------------------------------------------------------------------------

@dataclass
class MemoryRecord:
    """
    A single persisted memory entry.

    Mirrors the `memory_records` table schema.

    Fields:
        id:           UUID primary key (auto-generated).
        gaian_id:     The Gaian Twin this memory belongs to.
        content:      The memory content (text or JSON string).
        memory_type:  e.g. "episodic", "semantic", "shadow", "identity".
        session_id:   Session in which this memory was created.
        phi:          Coherence / integration score at time of creation.
        tags:         Searchable tag list.
        created_at:   Unix timestamp.
        metadata:     Arbitrary dict for extended fields.
    """
    id: str = field(default_factory=lambda: str(uuid.uuid4()))
    gaian_id: str = ""
    content: str = ""
    memory_type: str = "episodic"
    session_id: str = ""
    phi: float = 0.0
    tags: List[str] = field(default_factory=list)
    created_at: float = field(default_factory=time.time)
    metadata: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "gaian_id": self.gaian_id,
            "content": self.content,
            "memory_type": self.memory_type,
            "session_id": self.session_id,
            "phi": self.phi,
            "tags": self.tags,
            "created_at": self.created_at,
            "metadata": self.metadata,
        }


