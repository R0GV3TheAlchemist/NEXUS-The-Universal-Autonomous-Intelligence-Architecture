# AuditLog
# ---------------------------------------------------------------------------

@dataclass
class AuditLog:
    """
    A single audit log entry.

    Mirrors the `audit_logs` table schema.
    Used by permission system, safety controller, and Sentinel.

    Fields:
        id:         UUID primary key.
        actor:      Who performed the action (gaian_id, agent_id, or system).
        action:     What was done (e.g. "WRITE_MEMORY", "ISSUE_HALT").
        resource:   What was acted upon.
        outcome:    "granted" or "denied".
        reason:     Human-readable reason string.
        timestamp:  Unix timestamp.
        context:    Arbitrary dict for extended diagnostic fields.
    """
    id: str = field(default_factory=lambda: str(uuid.uuid4()))
    actor: str = ""
    action: str = ""
    resource: str = ""
    outcome: str = "granted"
    reason: str = ""
    timestamp: float = field(default_factory=time.time)
    context: Dict[str, Any] = field(default_factory=dict)

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "actor": self.actor,
            "action": self.action,
            "resource": self.resource,
            "outcome": self.outcome,
            "reason": self.reason,
            "timestamp": self.timestamp,
            "context": self.context,
        }


# ---------------------------------------------------------------------------
# SessionRecord
# ---------------------------------------------------------------------------

@dataclass
class SessionRecord:
    """
    A persisted GAIA session.

    Mirrors the `sessions` table schema.

    Fields:
        id:           Session UUID.
        gaian_id:     Owning Gaian Twin.
        started_at:   Unix timestamp of session start.
        ended_at:     Unix timestamp of session end (None if active).
        mode_history: List of (timestamp, mode) tuples.
        phi_peak:     Highest phi recorded during this session.
        metadata:     Arbitrary dict.
    """
    id: str = field(default_factory=lambda: str(uuid.uuid4()))
    gaian_id: str = ""
    started_at: float = field(default_factory=time.time)
    ended_at: Optional[float] = None
    mode_history: List[Dict[str, Any]] = field(default_factory=list)
    phi_peak: float = 0.0
    metadata: Dict[str, Any] = field(default_factory=dict)

    @property
    def is_active(self) -> bool:
        return self.ended_at is None

    def to_dict(self) -> Dict[str, Any]:
        return {
            "id": self.id,
            "gaian_id": self.gaian_id,
            "started_at": self.started_at,
            "ended_at": self.ended_at,
            "is_active": self.is_active,
            "mode_history": self.mode_history,
            "phi_peak": self.phi_peak,
            "metadata": self.metadata,
        }


