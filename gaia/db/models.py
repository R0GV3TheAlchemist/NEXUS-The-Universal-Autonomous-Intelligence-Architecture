# CanonRef
# ---------------------------------------------------------------------------

@dataclass
class CanonRef:
    """
    A reference to a canon document.
    Used in memory records, audit logs, and session metadata.

    Fields:
        canon_id:   e.g. "C167", "BWL-016", "PRIMORDIAL".
        title:      Human-readable title.
        tier:       CanonTier string value.
        path:       Relative path to the canon document.
    """
    canon_id: str
    title: str = ""
    tier: str = "NUMBERED"
    path: str = ""

    def to_dict(self) -> Dict[str, Any]:
        return {
            "canon_id": self.canon_id,
            "title": self.title,
            "tier": self.tier,
            "path": self.path,
        }
